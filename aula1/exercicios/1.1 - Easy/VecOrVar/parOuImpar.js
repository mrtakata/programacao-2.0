// Exercicio A - ler varios numeros e imprimir se o numero [e par ou impar
// Primeira versao
var limite;
var numero;
prompt(limite);
for (i = 0; i < limite; i++){
    prompt(numero);
	if (numero % 2 == 0) {
		print("O numero [e par");
	} else {
		print("O numero [e impar");
	}
}

